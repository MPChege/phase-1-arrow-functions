// Function expression for dividing two numbers
const divide = function(num1, num2) {
    return num1 / num2;
  }
  
  // Arrow function for squaring a number
  const square = (num) => {
    return num * num;
  }
  
  // Arrow function for adding two numbers
  const add = (num1, num2) => {
    return num1 + num2;
  }
  
  module.exports = { divide, square, add };
  